            $(document).ready(function(){
              var myPlaylists = new jPlayerPlaylist({
                jPlayer: "#jplayer_l",
                cssSelectorAncestor: "#jp_container_l"
              }, [
                <?php 

                        foreach ($albums as $row) {                         
                      ?>
                {
                  title:"<?php echo $row[0]->name ?>",
                  mp3:"<?php echo $row[0]->link ?>",
                  poster: "<?php echo base_url('assets/images/m4'.rand(0,5).'.jpg') ?>"
                },
                <?php }?>
              ], {
                playlistOptions: {
                  enableRemoveControls: true,
                  autoPlay: true
                },
                swfPath: "js/jPlayer",
                supplied: "webmv, ogv, m4v, oga, mp3",
                smoothPlayBar: true,
                keyEnabled: true,
                audioFullScreen: true
              });
  
              $(document).on($.jPlayer.event.pause, myPlaylists.cssSelector.jPlayer,  function(){
                $('.musicbar').removeClass('animate');
                $('.jp-play-song').removeClass('active');
                $('.jp-play-song').parent('li').removeClass('active');
              });

              $(document).on($.jPlayer.event.play, myPlaylists.cssSelector.jPlayer,  function(){
                $('.musicbar').addClass('animate');
              });

              $(document).on('click', '.jp-play-song', function(e){
                e && e.preventDefault();
                var $this = $(e.target);
                if (!$this.is('a')) $this = $this.closest('a');

                $('.jp-play-song').not($this).removeClass('active');
                $('.jp-play-song').parent('li').not($this.parent('li')).removeClass('active');

                $this.toggleClass('active');
                $this.parent('li').toggleClass('active');
                if( !$this.hasClass('active') ){
                  myPlaylists.pause();
                }else{
                  var i = $(this).attr('data-pos');
                  myPlaylists.play(i);
                }
                
              });
            });
            </script>